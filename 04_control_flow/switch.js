const month = 3

switch (month) {
    case 1:
        console.log("January");
        break;
    case 2:
        console.log("FEB");
        break;
    case 3:
        console.log("MARCH");
        break;
    case 4:
        console.log("April");
        break;
    default:
        console.log("Default case match");
        break;
}
